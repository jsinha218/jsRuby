##Relational Operators 
## >=,<=,>,<,==
## <=> general comparison operator gives -1(less than),0(equal),1(greater than)
##These return boolean values
a = 5 > 7
puts(a)
b = 10 < 67
puts(b)
c = 12 >= 34
puts(c)
d = 12 <= 12
puts(d)
e = 5 == 5
puts(e)
f = 'a' <=> 't'
puts(f)
g = 1 <=> 1
puts(g)
h = 'z' <=> 'a'
puts(h)

